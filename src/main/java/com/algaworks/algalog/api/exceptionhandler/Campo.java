package com.algaworks.algalog.api.exceptionhandler;

public class Campo {
	
	private String nome;
	
	private String mengagem;
	
	
	public Campo(String nome, String mengagem) {
		super();
		this.nome = nome;
		this.mengagem = mengagem;
	}

	public String getNome() {
		return nome;
	}

	public String getMengagem() {
		return mengagem;
	}
	
	

}
